function bat_clearForms(): void {
  for (const ef of $effects`Wolf Form, Mist Form, Bats Form`) {
    if (0 !== haveEffect(ef)) {
      useSkill(toSkill(ef));
    }
  }
}

function bat_switchForm(form: Effect, speculative: boolean): boolean {
  if (0 !== haveEffect(form)) {
    return true;
  }
  if (!haveSkill(toSkill(form))) {
    if (!speculative) {
      bat_clearForms();
    }
    return false;
  }
  if (myHp() <= 10) {
    if (!speculative) {
      auto_log_warning(
        `We don't have enough HP to switch form to ${form}!`,
        "red",
      );
    }
    return false;
  }
  if (speculative) {
    return true;
  }
  return useSkill(1, toSkill(form));
}

function bat_switchForm$1(form: Effect): boolean {
  return bat_switchForm(form, false);
}

export function bat_formPreAdventure(): boolean {
  if (!in_darkGyffte()) {
    return false;
  }

  const desiredForm: string = getProperty("auto_bat_desiredForm");
  const form: Effect = Effect.none;
  switch (desiredForm) {
    case "wolf":
      return bat_switchForm$1($effect`Wolf Form`);
    case "mist":
      return bat_switchForm$1($effect`Mist Form`);
    case "bats":
      return bat_switchForm$1($effect`Bats Form`);
    case "":
      bat_clearForms();
      return true;
    default:
      auto_log_error(
        `auto_bat_desiredForm was set to bad value: '${desiredForm}'. Should be '', 'wolf', 'mist', or 'bats'.`,
      );
      setProperty("auto_bat_desiredForm", "");
      return false;
  }
}

